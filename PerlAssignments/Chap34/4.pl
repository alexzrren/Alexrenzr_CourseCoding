use warnings;
use strict;
my $seq='';
my $unique=0;
my $total=0;
my $cg=0;
while(<DATA>)
{
	my $head=substr($_,0,1);
	if($head eq '>')
	{
		my @arr=split(/_/,substr($_,1));
		$unique++; #Task.c Unique count
		$total+=$arr[2]; #Task.d Total count
	}
	else #Join Sequence
	{
		chomp($_);
		$seq=$seq.$_;
	}
}
my $seqlen=length($seq);
my @seq=split(//,$seq);
my $count=0;
foreach $_(@seq) #Scan sequence
{
	if($_ eq 'C' || $_ eq 'G')
	{
		$cg++; #Task.b G+G count
	}
	 #Task.a print80 bases as a line
	if($count<80)
	{
		print"$_";
		$count++;
	}
	else
	{
		print"\n$_";
		$count=1;
	}
}
my $per=100*$cg/$seqlen;
print"\n";
print"num of C+G:$cg, percentage:$per%\n";
print"unique:$unique\n";
print"total:$total\n";

